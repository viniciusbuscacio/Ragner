"""
Cores - fornece cores para o terminal.
"""

# Criar classe para cores ANSI
class Cores:
    """
    Classe para definir cores ANSI para o terminal.
    """
    AMARELO = "\033[93m"
    AZUL = "\033[94m"
    VERDE = "\033[92m"
    VERMELHO = "\033[91m"
    MAGENTA = "\033[95m"
    CINZA = "\033[90m"
    BRANCO = "\033[97m"
    RESET = "\033[0m"

